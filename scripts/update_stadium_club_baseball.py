# Check what's missing
def get_is_id(name):
    row = db.execute("SELECT id FROM insert_sets WHERE set_id = ? AND name = ?", (SET_ID, name)).fetchone()
    return row[0] if row else None

def has_parallel(is_id, name):
    return db.execute("SELECT 1 FROM parallels WHERE insert_set_id = ? AND name = ?", (is_id, name)).fetchone() is not None

def add_if_missing(is_id, name, print_run):
    if not has_parallel(is_id, name):
        db.execute("INSERT INTO parallels (insert_set_id, name, print_run) VALUES (?, ?, ?)", (is_id, name, print_run))
        return True
    return False

